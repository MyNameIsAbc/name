package excel;

public class ModelName {
	String name, mobile;

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getMobile() {
		return mobile;
	}

	public void setMobile(String mobile) {
		this.mobile = mobile;
	}

	@Override
	public String toString() {
		return "ModelName [name=" + name + ", mobile=" + mobile + "]";
	}
}
